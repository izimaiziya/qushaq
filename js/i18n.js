let CURRENT_LANG = localStorage.getItem('qushaq_lang') || 'ru';

function t(key, fallback) {
  const dict = I18N[CURRENT_LANG] || I18N.ru;
  if (dict[key] !== undefined) return dict[key];
  if (I18N.ru[key] !== undefined) return I18N.ru[key];
  return fallback !== undefined ? fallback : key;
}

function applyTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  root.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
  root.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    el.setAttribute('placeholder', t(key));
  });
  root.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const spec = el.getAttribute('data-i18n-attr');
    spec.split(';').forEach(pair => {
      const [attr, key] = pair.split(':').map(s => s.trim());
      if (attr && key) el.setAttribute(attr, t(key));
    });
  });
  document.documentElement.lang = CURRENT_LANG;
}

function setLanguage(lang) {
  if (!I18N[lang]) return;
  CURRENT_LANG = lang;
  localStorage.setItem('qushaq_lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('on', btn.getAttribute('data-lang') === lang);
  });
  applyTranslations(document);
  if (typeof onLanguageChanged === 'function') onLanguageChanged();
}

window.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('on', btn.getAttribute('data-lang') === CURRENT_LANG);
    btn.addEventListener('click', () => setLanguage(btn.getAttribute('data-lang')));
  });
  applyTranslations(document);
});
